package week4.day5.codeup.c1077;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();

        for (int i = 0; i <= a; i++) {
            System.out.println(i);
        }
        sc.close();
    }
}
